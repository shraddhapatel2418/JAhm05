package firstclass;

public class firstclass {
	public static void main(String[] args) {
		int a=10,b=20;
		int result;
		
		result=a+b;
		System.out.println(a +"+"+ b+" ==> "+result);
		
		result=b-a;
		System.out.println(b +"-"+ a+" ==> "+result);
		
		result=a*b;
		System.out.println(a +"*"+ b+" ==> "+result);
		
		result=b/a;
		System.out.println(a +"/"+ b+" ==> "+result);
	}
}
